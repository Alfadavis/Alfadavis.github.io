	<h2>Introduccion a PHP y las estructuras de programacion</h2>
	<p>
		PHP (Hypertext Preprocessor) es un lenguaje de programacion de codigo abierto ampliamente utilizado para el desarrollo web.  Fue creado en 1994 y es gratuito y de código abierto, PHP se ejecuta en el servidor y puede generar contenido dinamico en las paginas web. Algunas de las caracteristicas de PHP incluyen la capacidad de interactuar con bases de dato, el manejo de formularios, y la gestion de archivos, entre otras.
	</p>
	<p>
		Las estructuras de programacion en PHP son similares  a las de otros lenguajes de programacion. estas estructuras permiten controlar el flujo de la ejecucion del codigo y realizar tareas especificas de manera eficiente. Las principales estructuras de programacion en PHP son:
	</p>

	<ul>
		<li><strong>Secuenciales:</strong>Las instrucciones se ejecutan de manera secuencial, una tras otra.</li>
		<li><strong>Condicionales:</strong>Permiten tomar decisiones y ejecutar diferentes bloques de codigo segun se cumpla ciertas condiciones. Ejemplos de estructuras condicionales son 
		'if', 'else', 'elseif' y 'switch.</li>
		<li><strong>Bucles:</strong>Permiten ejecutar un bloque de codigo varias veces, segun se cumplan ciertas condiciones. Ejemplos de blucles son 'for', 'while' y 'do-while.</li>

	</ul>

	<p>
		Estas estructuras de programacion se pueden combinar de diversas formas para crear algoritmos y soluciones mas complejas y eficientes.
	</p>



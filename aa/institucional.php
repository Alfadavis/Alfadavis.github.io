	 		<b><h2 align="center">MODELO PEDAGOGICO</h2></b>
	 		
	 		<p>El modelo pedagógico que se está implementando en la I.E José Antonio Galán, es el modelo pedagógicos que basado en un aprendizaje social, el cual se basa en la identificación de  situación social en la que, al menos, participan dos personas: el modelo, que realiza una conducta determinada, y el sujeto, que realiza la observación de dicha conducta y cuya observación determina el aprendizaje. </p>
         <p>  Algunos autores como Albert Bandura formula la teoría de este aprendizaje por
	 		observación a partir de las fases de atención (debe llamar la atención), retención (debe poderse representar mentalmente), reproducción (llevar a la práctica) y reforzamiento (motivación a través de la eficacia). Estas fases nos indican el proceso del aprendizaje social. Bandura realizó la experimentación en el aprendizaje de niños por observación. Lev Vygotsky. Formula la idea de que el conocimiento se construye por medio de operaciones y habilidades cognoscitivas, que se inducen a través de la interacción social. El plano social transmite conocimiento al plano individual. Evidentemente, el plano social de Vygotsky se puede trasladar a la red social; por tanto sus teorías se pueden aplicar al aprendizaje social. George Siemens con su teoría del conectivismo, que entre otras cosas dice que el aprendizaje es el proceso de conectar nodos o fuentes de información.  
En esta medida el modelo pedagógico de la institución educativa se permea desde lo social y se tiene que evidenciar desde enfoques pedagógicos que beneficien estos aspectos. Dicho así, los  enfoques que le apuntan al constructivismo, a lo crítico social, a las competencias, a los aprendizajes significativos implementados  desde los problemas sociales y la cognición situada son enfoques útiles en la implementación de este modelo, lo importante es que la interacción del medio en que vivimos y todos sus problemas nos permitan aprender.</p>

	 		
